

public class GetLoopy
{
    public static void main(String[] args) {





        for(int row1 = 5; row1 >= 0; row1--){

            System.out.print("*");
            System.out.println();

            for(int col1 = 5;  col1 >= row1; col1--){

                System.out.print("*");




            }

        }




























    }
}